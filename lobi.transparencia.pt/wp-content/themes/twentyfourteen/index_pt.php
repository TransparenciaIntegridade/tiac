<h2 style="margin-top:50px;color:#124964;"> LEVANTAR O VÉU DO LÓBI: ACABAR COM O SECRETISMO NA POLÍTICA NA EUROPA   </h2>
				<h3 style="margin-top:30px;color:#0074A2;">Resumo do Projecto Europeu:</h3>
				<p style="text-align:justify">O projecto sobre lóbi surge no seguimento do projecto europeu Sistema Nacional de Integridade (SNI), na recolha de dados sobre corrupção no espaço europeu e no qual Portugal participou. No relatório europeu decorrente do SNI, “Money, Politics, Power: Corruption Risks in Euope”, práticas obscuras de lóbi e legislação fraca ou inexistente destacaram-se como prioridades fundamentais no combate à corrupção na Europa. Para além de Portugal, este novo projecto junta 18 capítulos nacionais da Transparency Internacional (TI): Alemanha, Áustria, Bulgária, Chipre, Eslováquia, Eslovénia, Espanha, Estónia, França, Hungria, Irlanda, Itália,  Letónia, Lituânia, Reino Unido e a Representação da TI na União Europeia.</p>
<p style="text-align:justify">
		O projecto, originalmente denominado “Lifting the Lido n Lobbying: Taking the secrecy out of politics in Europe” é financiado a 90% pela DG Home Affairs da Comissão Europeia. Tem um orçamento global de 1.235.283,70 € e uma duração de 2 anos, tendo tido início a 1 de Novembro de 2014.
		
		
		</p>		
				<h3 style="color:#124964;margin-top:30px;" > TRABALHO DE FUNDO  </h3>
<p style="text-align:justify">
	
	Para que o lóbi seja uma força positiva e legítima, o acesso igualitário de todos os grupos de interesse aos decisores políticos é crucial. No entanto, os recursos financeiros à disposição de determinados grupos de interesse e a proximidade entre o sector privado e a política podem levar a influências indevidas na delineação das políticas públicas de um país e potenciar os riscos de corrupção política. A confiança da opinião pública sobre a forma como as decisões políticas e administrativas são tomadas (e no interesse de quem) tem sido afectada negativamente por repetidas práticas obscuras de lóbi. No actual contexto de crise na Europa, o sector financeiro tem suscitado uma preocupação especial junto dos cidadãos europeus e dos decisões públicos.
	
	
	
	</p>
	<p style="text-align:justify">
	Este projecto procura assim aprofundar a transparência e accountability das práticas de lóbi na Europa. 
	</p>
