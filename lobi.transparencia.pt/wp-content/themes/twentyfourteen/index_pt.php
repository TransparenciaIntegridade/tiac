	<h3 style="color:#124964;margin-top:100px">CASO DE ESTUDO 2</h3>
		<h3 style="color:#124964;margin-top:50px">O lóbi da energia: “Um Estado dentro do Estado”</h3>
			<p style="text-align:justify">Tem havido em Portugal um amplo debate sobre as excessivas rendas concedidaspelo Estado ao sector da energia. O Memorandum de Entendimento assinado com a Troika inclui disposições destinadas a minimizar o csize="30"omportamento de procura de rendas (rent-seeking). Já no final do programa de assistência financeira, a Troika
demonstrou a sua insatisfação e alertou mais uma vez relativamente às rendas excessivas no sector da energia. </p>

	<p style="text-align:justify">
	
Em 2012, Henrique Gomes, Secretário de Estado da Energia, procurou reformar o sector energético e "impor o interesse público ao excessivo o poder da EDP". O executivo encomendou um estudo internacional sobre o setor de energia, que concluiu que este beneficiava de 300 milhões de euros de rendas.2 O estudo recomendava cortes e taxas sobre as rendas excessivas e Henrique Gomes estava disposto a segui-las. Após ter sido impedido de fazer uma declaração pública sobre o assunto, Gomes pediu a demissão, alegando "razões pessoais". Na altura, diferentes meios de comunicação sugeriram que o Secretário de Estado tinha perdido a última guerra contra empresas de energia. Mais tarde, esclareceu os motivos de sua demissão, admitindo que tinha saído por o governo não estar disposto a defender o interesse público. Acrescentou que “toda a gente sabe que há uma grande proximidade entre o Governo e as grandes empresas” e que, no sector da energia, estas "estão muito bem organizadas e dominam a imprensa, os consultores, os advogados". Em audiência parlamentar, Gomes declarou que António Mexia, CEO da EDO, tinha tido acesso ao relatório confidencial, logo após este ter sido entregue ao Primeiro-ministro.
O governo nunca veio a público negar estas acusações. Quase um ano depois do episódio, o Ministro da Economia admitiu que a demissão do seu secretário de Estado tinha levado à “abertura de garrafa de champanhe” por parte de certas pessoas. Ao comentar a polémica, um antigo ministro da energia afirmou que o atual ministro não tinha sido suficientemente forte para comba
		ter o lóbi da energia, ligando ainda o lóbi do ambiente devido aos grandes investimentos que o país tem vindo a fazer na produção de energia limpa em parceria com a EDP.
		</p>
		<p style="text-align:justify">
	
	Antes de se tornar CEO da EDP, António Mexia foi Ministro das Obras Públicas, Transportes e Comunicações num governo PSD-CDS (2004-05) e CEO da Galp (outra grande empresa do sector da energia). A EDP apresenta ainda ligações a outros 18 políticos.
</p>
