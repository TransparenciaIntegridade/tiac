	<h3 style="color:#124964;margin-top:100px;">CASE STUDY 1</h3>
		<h3 style="color:#124964;margin-top:50px">Transparência no Financiamento Político</h3>
			<p style="text-align:justify">A proibição de donativos de empresas tem sido contornada por uma certa elite empresarial. No seguimento de escândalos no setor financeiro, foi exposto que muitos banqueiros doaram grandes somas a candidatos durante a campanha presidencial de
2006. Por exemplo, cinco membros da família Espírito Santo, dona de um dos maioresgrupos financeiros em Portugal,
doaram a um dos candidatos o limite máximo porpessoa, num total de 2,2 milhões de euros (5% do orçamento total da campanha).
Três outros presidentes de instituições bancárias foram também identificados como grandesdoadores a candidatos presidenciais.</p>

	<p style="text-align:justify">
	
Apesar de não apresentarem nada de ilegal, estas doações individuais provam, maisuma vez, a relação de forte proximidade entre política e negócios. Mostra igualmente que há falta de uma transparência em tempo útil e que o mecanismo de supervisão do financiamento partidário é desanimador a esse respeito. Os partidos políticos e as campanhas eleitorais são obrigados a apresentar as suas contas a uma entidade independente, mas essas contas e seu controle não são tornadas públicas em tempoútil. Por exemplo, os relatórios das eleições presidenciais de 2011 não estão ainda disponíveis ao público. Um registo público e imediato de doações a partidos políticos e das campanhas eleitorais melhoraria a transparência e a prestação de contas e ajudaria os eleitores a fazerem escolhas eleitorais mais informadas. 
	
		</p>

