				<h3>A nossa Organização</h3>
				<p>

A TIAC – TRANSPARÊNCIA E INTEGRIDADE, ASSOCIAÇÃO CÍVICA é uma organização anti-corrupção, representante em Portugal da rede global anti-corrupção Transparency International.

Trabalhamos para uma sociedade mais justa e uma democracia de qualidade em Portugal.

Queremos uma sociedade onde exista um acesso efetivo à informação por parte de uma cidadania informada, forte e participativa. Uma sociedade onde exista uma regulação eficaz dos sistemas e organizações, uma Justiça célere, eficiente e ética e um verdadeiro sentido de responsabilidade social por parte de todos os agentes económicos.

Para isso, trabalhamos em cinco vertentes específicas:

1- Sensibilização pública dos cidadãos, desenvolvendo campanhas de informação, conferências, materiais educativos e iniciativas públicas sobre os temas da corrupção.

2- Investigação & Desenvolvimento, através da produção de relatórios, índices e dados estatísticos que permitam um conhecimento mais profundo e sistemático sobre a corrupção e o seu combate a nível nacional e internacional.

3- Monitorização de progressos, medindo e avaliando o grau de cumprimento, por Portugal, das convenções internacionais de que o Estado português é subscritor em matéria de corrupção e promoção do Governo Aberto e da transparência pública.

4- Lóbi político e advocacia social, trabalhando junto das autoridades públicas para que implementem em Portugal as melhores práticas de controlo e combate ao fenómeno da corrupção, nomeadamente através de reformas legislativas e da adoção de uma verdadeira Estratégia Nacional Contra a Corrupção.

5- Educação e apoio a denunciantes, prestando assistência aos cidadãos empenhados em denunciar suspeitas de corrupção às autoridades competentes.

				</p>
				<p>
A TIAC foi criada no dia 17 de setembro de 2010, data em que se tornou oficialmente Ponto de Contacto em Portugal da Transparency International (TI), que coordena e apoia um conjunto de organizações da sociedade civil a nível global que combatem a corrupção. </p>
