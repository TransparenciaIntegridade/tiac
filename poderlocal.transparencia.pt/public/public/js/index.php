				<p>A TIAC definiu um conjunto de temas para os quais serão desencadeadas ações prioritárias durante o período 2009-2014:</p>
                
   <p> Corrupção política (financiamento político, conflitos de interesse e declarações patrimoniais);
    Corrupção e má gestão na administração pública (saúde, educação, justiça, defesa, ambiente, fronteiras e imigração, grandes obras públicas);
