                </div>
				<p style="text-align:left;">O Índice de Perceção da Corrupção (CPI) é um indicador agregado que combina diferentes fontes de informação, classificando os países de acordo com a perceção sobre o fenómeno da corrupção no setor público.</p>
                <p style="text-align:left;">Este índice tem por base diferentes avaliações e inquéritos de opinião realizados por instituições respeitáveis e independentes. De uma forma geral, os levantamentos e avaliações utilizados para compilar o índice incluem questões relativas à corrupção de funcionários públicos, subornos nos contratos públicos, desvios de fundos públicos, assim como perguntas que visam aferir a força e eficácia dos esforços anticorrupção na administração pública.</p>
                <p style="text-align:left;">A escolha das perceções advém não só da dificuldade em medir – em termos de frequência ou quantidade – a corrupção, por se tratar de uma atividade escondida, mas também do pressuposto de que as perceções constituem uma estimativa fiável no âmbito do tema da corrupção.</p>
                <p style="text-align:left;">Lançado pela primeira vez em 1995, o CPI é, atualmente, a ferramenta mais conhecida da Transparency International.</p>
                <p style="text-align:left;">Para mais informação sobre o CPI, visite o portal da Transparency International.</p>
