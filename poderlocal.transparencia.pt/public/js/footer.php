<div id="corrupcao" ><h3 style="text-align:center">O que é a corrupção?</h3>

<p style="text-align:justify;margin-left:1em;">A Transparência Internacional (TI) usa uma definição clara e precisa do termo: Corrupção corresponde ao abuso do poder confiado para ganhos privados. <p style="text-align:justify;margin-left:1em;">A TI faz ainda uma distinção entre corrupção “de acordo com a norma” e corrupção “contra a norma”. No primeiro caso, enquadra-se por exemplo a facilitação de pagamentos, isto é, quando um suborno é pago com o intuito de obter um tratamento preferencial por algo que o recetor do suborno já é obrigado a fazer por lei.</p>
<p style="text-align:justify;margin-left:1em;">No segundo caso, por sua vez, enquadram-se as situações em que é pago um suborno para obter serviços cuja execução por parte do recetor do suborno é proibida por lei.</p>
  </div>
</div>



                <div style="display:none">
<div id="transparencia" ><h3 style="text-align:center">O que é a transparência?</h3>

<p style="text-align:justify;margin-left:1em;">Transparência pode ser definida como o princípio que permite a todos os que de alguma forma são afetados por decisões administrativas, transações de negócios ou trabalhos de caridade conhecer não só os factos e os números mais elementares, mas também os mecanismos e processos envolvidos naquelas ações.</p> <p style="text-align:justify;margin-left:1em;">Constitui um dever alargado a funcionários públicos, gestores e administradores, contribuindo para que estes ajam de uma forma percetível, previsível e compreensível.</p>
  </div>
</div>


 <div style="display:none">
<div id="custos" ><h3 style="text-align:center">Quais são os custos da corrupção?</h3>

<p style="text-align:justify;margin-left:1em;">Os custos da corrupção são transversais a diversos campos: político, económico, social e ambiental. No campo da política, a corrupção constitui um grande obstáculo à democracia e ao Estado de direito. Num sistema democrático, as empresas e as instituições perdem a legitimidade quando são usadas de forma abusiva para proveitos privados.</p>
<p style="text-align:justify;margin-left:1em;">Uma liderança política responsável não se pode desenvolver num clima corrupto. Economicamente, a corrupção extenua a riqueza nacional, sendo muitas vezes responsável pela canalização de recursos públicos escassos para projetos de alta visibilidade mas sem rentabilidade, em detrimento de projetos com menos aparato, mas fundamentais para a qualidade de vida das populações, como escolas, hospitais e estradas ou o fornecimento de energia e água a zonas rurais.</p>
<p style="text-align:justify;margin-left:1em;">A TI aborda estas questões no Bribery Payers Index (BPI), uma ferramenta complementar ao Corruption Perceptions Index (CPI). Para além da questão regional, é destacada a influência do setor de atividade no que diz respeito aos níveis de corrupção. O BPI fornece evidências estatísticas sobre os setores de negócios mais permeáveis a práticas corruptas. De acordo com os seus resultados mais recentes (2011), a construção encontra-se entre os setores de maior risco, enquanto a agricultura ocupa o último lugar da tabela.</p>
  </div>
</div>

               
               
               
                
                <div style="display:none">
<div id="quantificar" ><h3 style="text-align:center">É possível quantificar os custos da corrupção?</h3>

<p style="text-align:justify;margin-left:1em;">A resposta curta é “não”. Alguns especialistas utilizam análises de regressão e outros métodos empíricos para traduzir monetariamente os custos da corrupção.No entanto, é praticamente impossível fazer estes cálculos, na medida em que os pagamentos de subornos não são registados publicamente.</p> <p style="text-align:justify;margin-left:1em;">Ninguém sabe exatamente quanto dinheiro é anualmente “investido” em funcionários corruptos. Além do mais, o próprio suborno não se restringe à questão monetária: favores, “jeitinhos” e presentes são práticas comuns.No máximo, é possível investigar a correlação entre os níveis de corrupção e, digamos, de democratização, de desenvolvimento económico ou de degradação ambiental. </p><p style="text-align:justify;margin-left:1em;">Os custos sociais da corrupção são ainda menos quantificáveis, na medida em que seria inadequado medir a tragédia humana em termos de custos monetários. É, portanto, justificável a existência de um ceticismo geral em relação à tentativa de quantificar os custos da corrupção.</p>
  </div>
</div>

 
   <div style="display:none">
<div id="contextos" ><h3 style="text-align:center">Em que contextos existem maiores níveis de corrupção?</h3>

<p style="text-align:justify;margin-left:1em;">À primeira vista, o Índice de Perceção da Corrupção (CPI) publicado anualmente pela TI parece confirmar a ideia estereotipada de que a corrupção é um problema que afeta predominantemente os países do Sul. Enquanto os países escandinavos ocupam o topo do ranking, a maioria dos lugares da base são ocupados por países da África Subsariana. O CPI não tem como objetivo, todavia, realçar determinados países ou fazer a oposição Norte/Sul. Pelo contrário, é uma ferramenta de sensibilização da opinião pública para o problema da corrupção e de promoção de uma melhor governação. A corrupção é um problema transversal a todos os países, desenvolvidos ou em desenvolvimento. Os recentes escândalos tanto ao nível nacional, como em países como a Alemanha, França, Japão, EUA ou Reino Unido confirmam esta realidade. É a existência de um sistema de controlo, monitorização e prevenção bem estabelecido que marca a diferença. Em qualquer país, as pessoas são tão corruptas quanto o sistema permite que sejam. Onde a tentação é acompanhada pela permissividade, a corrupção enraíza-se em larga escala. Até muito recentemente, os governos do Norte não só toleravam estas práticas como as incentivavam, através do direito à dedução de imposto. Felizmente, com a entrada em vigor da Convenção Anti-Suborno da OCDE, em 1999, o suborno de funcionários públicos estrangeiros passou a constituir uma ofensa criminal.</p>
  </div>
</div>               
             
               
                <div style="display:none">
<div id="sociedades" ><h3 style="text-align:center">A corrupção não é uma prática normal ou tradicional em determinadas sociedades?</h3>

<p style="text-align:justify;margin-left:1em;">Alguns críticos argumentam que a luta contra a corrupção representa mais um caso em que o Norte tenta impor os seus pontos de vista e valores ao Sul. Muitos poderão afirmar que o ato de dar e receber na esfera pública constitui uma tradição normal em muitas culturas não-ocidentais. Os defensores do relativismo cultural poderão argumentar que onde não existem conceitos como procedimentos de contratação pública, não existem subornos para obter contratos de obras públicas. As normas e os valores estão vinculados ao contexto e variam entre as culturas. As ofertas fazem parte da negociação e construção de relações em algumas partes do mundo. No entanto, o relativismo cultural termina onde a conta na Suíça entra em cena.A organização da rede global TI em capítulos nacionais visa precisamente lidar da melhor forma com estas diferenças culturais. Os especialistas e ativistas sediados em cada país sabem avaliar melhor do que ninguém o que é uma mera tradição ou prática cultural e o que constitui uma infração à regra num determinado país. Desta forma, a rede TI consegue ser mais eficaz na luta contra a corrupção e na promoção da transparência e da boa governança. Mas, claramente, o abuso de poder para proveito pessoal ou o desvio de recursos públicos para os bolsos privados são inaceitáveis em qualquer cultura ou sociedade.</p>
  </div>
</div>   
 
                <div style="display:none">
<div id="democracia" ><h3 style="text-align:center">Democracia e corrupção: realidades (irre)conciliáveis?</h3>

<p style="text-align:justify;margin-left:1em;">Numa democracia moderna, o poder é confiado e é suposto ser usado para benefício da sociedade em geral, e não para benefício pessoal do indivíduo que o detém. Com efeito, a corrupção – abuso do poder público confiado para fins privados – é intrinsecamente contraditória e irreconciliável com a democracia. Isso não significa, infelizmente, que a corrupção não ocorra nos sistemas democráticos. A tentação continua a ser um desafio em qualquer país e sistema do mundo. É por isso que continua a ser tão premente desenvolver e implementar mecanismos de controlo local e estabelecer barreiras sistémicas para evitar que as pessoas abusem do poder que lhes é confiado. A elaboração e implementação destes mecanismos é, todavia, mais fácil em contextos em que existe democracia estabelecida, do que em países recém-democráticos ou não-democráticos.</p>
  </div>
